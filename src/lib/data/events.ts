// Helper: dates relative to "now" (April 10, 2026)
const d = (daysOffset: number, time: string = '12:00') => {
  const base = new Date('2026-04-10T00:00:00');
  base.setDate(base.getDate() + daysOffset);
  return base.toISOString().split('T')[0] + 'T' + time;
};

const date = (daysOffset: number) => {
  const base = new Date('2026-04-10T00:00:00');
  base.setDate(base.getDate() + daysOffset);
  return base.toISOString().split('T')[0];
};

